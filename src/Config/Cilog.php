<?php 

namespace XVIID\Cilog\Config;

use CodeIgniter\Config\BaseConfig;

class Cilog extends BaseConfig
{
	/**
	 * --------------------------------------------------------------------
	 * Path
	 * --------------------------------------------------------------------
	 *
     * Path to save logs.
	 *
	 * @var string
	 */
    public static $path;

	/**
	 * --------------------------------------------------------------------
	 * File Path
	 * --------------------------------------------------------------------
	 *
     * File to save logs.
	 *
	 * @var string
	 */
    public static $filepath;

	/**
	 * --------------------------------------------------------------------
	 * File Name
	 * --------------------------------------------------------------------
	 *
     * Current file name that is saved in the log.
	 *
	 * @var string
	 */
    public static $filename;

	/**
	 * --------------------------------------------------------------------
	 * Log
	 * --------------------------------------------------------------------
	 *
     * Contents of the current log.
	 *
	 * @var string
	 */
    public static $log;

	/**
	 * --------------------------------------------------------------------
	 * Log
	 * --------------------------------------------------------------------
	 *
     * Array with logs.
	 *
	 * @var array
	 */
    public static $logs = null;

	/**
	 * --------------------------------------------------------------------
	 * Log
	 * --------------------------------------------------------------------
	 *
     * Maximum number of saved logs.
	 *
	 * @var int
	 */
    public static $logNumber;

	/**
	 * --------------------------------------------------------------------
	 * Counter Logs
	 * --------------------------------------------------------------------
	 *
     * Counter logs.
	 *
	 * @var int
	 */
    public static $counterLogs = 0;

	/**
	 * --------------------------------------------------------------------
	 * States
	 * --------------------------------------------------------------------
	 *
     * Different states for logs.
	 *
	 * @var string
	 */
    public static $states;
}
